// Array.shift()
// Remove the first element of an array

const myArray = [1, 2, 3]
console.log('const myArray = [1, 2, 3]:', myArray)

myArray.shift()

console.log('myArray.shift():', myArray)

const removedElement = myArray.shift()

console.log('const removedElement = myArray.shift():', myArray, '   removedElement:', removedElement)