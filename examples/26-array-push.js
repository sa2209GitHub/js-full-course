// Array.push()
// Add a new element to the end of an array

const myArray = [1, 2, 3]
console.log('const myArray = [1, 2, 3]:', myArray)

myArray.push(4)
console.log('myArray.push(4):', myArray)

myArray.push(true)
console.log('myArray.push(true):', myArray)

myArray.push('John')
console.log('myArray.push(\'John\'):', myArray)