// Array Methods

// Array.push()
const myArray = [1, 2, 3]
console.log('Original myArray:', myArray)

myArray.push(4)
console.log('Add value into myArray, using myArray.push(4) at index[3]:', myArray)

myArray.push(true)
console.log('Add value into myArray, using myArray.push(true) at index[4]:', myArray)

myArray.push('John')
console.log('Add value into myArray, using myArray.push(\'John\') at index[5]:', myArray)