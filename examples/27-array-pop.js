// Array.pop()
// Remove the last element of an array

const myArray = [1, 2, 3]
console.log('const myArray = [1, 2, 3]:', myArray)

myArray.pop()

console.log('myArray.pop(): ', myArray)

const removedElement = myArray.pop()

console.log('const removedElement = myArray.pop(): ', myArray, '    removedElement: ', removedElement)
