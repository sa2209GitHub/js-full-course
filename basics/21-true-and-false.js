// True / False

console.log('0, !0, !!0 :', Boolean(0), Boolean(!0), Boolean(!!0))
console.log('\'\', !\'\', !!\'\' :', Boolean(''), Boolean(!''), Boolean(!!''))
console.log('undefined, !undefined, !!undefined :', Boolean(undefined), Boolean(!undefined), Boolean(!!undefined))
console.log('null, !nul, !!null :', Boolean(null), Boolean(!null), Boolean(!!null))

console.log('10, !10, !!10 :', Boolean(10), Boolean(!10), Boolean(!!10))
console.log('\'Name\', !\'Name\', !!\'Name\' :', Boolean('Name'), Boolean(!'Name'), Boolean(!!'Name'))

console.log('true || true :', true || true)
console.log('false || true :', false || true)
console.log('true || false :', true || false)
console.log('false || false :', false || false)

console.log('true && true :', true && true)
console.log('false && true :', false && true)
console.log('true && false :', true && false)
console.log('false && false :', false && false)
